public class Skills {
    private String skill,skillRating;
    public Skills() {
        this.skill = skill;
        this.skillRating = skillRating;
    }

    public String getSkill() {
        return skill;
    }

    public void setSkill(String skill) {
        this.skill = skill;
    }

    public String getSkillRating() {
        return skillRating;
    }

    public void setSkillRating(String skillRating) {
        this.skillRating = skillRating;
    }
}
